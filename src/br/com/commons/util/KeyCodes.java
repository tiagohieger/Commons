	/**
	 * 
	 * @param keyCode
	 * @param isModifier
	 *            Alt or Ctrl or Shift
	 * @return
	 */
	public static char toChar(final int keyCode, final boolean isAlt, final boolean isCtrl, final boolean isShift) {

		switch (keyCode) {
		// ///////////////////////
		case KEY_ZERO:
			return isShift ? ')' : '0';
		case KEY_ONE:
			return isShift ? '!' : isAlt && isCtrl ? '¹' : '1';
		case KEY_TWO:
			return isShift ? '@' : isAlt && isCtrl ? '²' : '2';
		case KEY_THREE:
			return isShift ? '#' : isAlt && isCtrl ? '³' : '3';
		case KEY_FOUR:
			return isShift ? '$' : isAlt && isCtrl ? '£' : '4';
		case KEY_FIVE:
			return isShift ? '%' : isAlt && isCtrl ? '¢' : '5';
		case KEY_SIX:
			return isShift ? '¨' : isAlt && isCtrl ? '¬' : '6';
		case KEY_SEVEN:
			return isShift ? '&' : '7';
		case KEY_EIGHT:
			return isShift ? '*' : '8';
		case KEY_NINE:
			return isShift ? '(' : '9';
		// ///////////////////////
		case KEY_NUM_ZERO:
			return '0';
		case KEY_NUM_ONE:
			return '1';
		case KEY_NUM_TWO:
			return '2';
		case KEY_NUM_THREE:
			return '3';
		case KEY_NUM_FOUR:
			return '4';
		case KEY_NUM_FIVE:
			return '5';
		case KEY_NUM_SIX:
			return '6';
		case KEY_NUM_SEVEN:
			return '7';
		case KEY_NUM_EIGHT:
			return '8';
		case KEY_NUM_NINE:
			return '9';
		// ///////////////////////
		case KEY_A:
			return isShift ? 'A' : 'a';
		case KEY_B:
			return isShift ? 'B' : 'b';
		case KEY_C:
			return isShift ? 'C' : 'c';
		case KEY_D:
			return isShift ? 'D' : 'd';
		case KEY_E:
			return isShift ? 'E' : 'e';
		case KEY_F:
			return isShift ? 'F' : 'f';
		case KEY_G:
			return isShift ? 'G' : 'g';
		case KEY_H:
			return isShift ? 'H' : 'h';
		case KEY_I:
			return isShift ? 'I' : 'i';
		case KEY_J:
			return isShift ? 'J' : 'j';
		case KEY_K:
			return isShift ? 'K' : 'k';
		case KEY_L:
			return isShift ? 'L' : 'l';
		case KEY_M:
			return isShift ? 'M' : 'm';
		case KEY_N:
			return isShift ? 'N' : 'n';
		case KEY_O:
			return isShift ? 'O' : 'o';
		case KEY_P:
			return isShift ? 'P' : 'p';
		case KEY_Q:
			return isShift ? 'Q' : 'q';
		case KEY_R:
			return isShift ? 'R' : 'r';
		case KEY_S:
			return isShift ? 'S' : 's';
		case KEY_T:
			return isShift ? 'T' : 't';
		case KEY_U:
			return isShift ? 'U' : 'u';
		case KEY_V:
			return isShift ? 'V' : 'v';
		case KEY_W:
			return isShift ? 'W' : 'w';
		case KEY_X:
			return isShift ? 'X' : 'x';
		case KEY_Y:
			return isShift ? 'Y' : 'y';
		case KEY_Z:
			return isShift ? 'Z' : 'z';
		// ///////////////////////
		case KEY_CEDILLA:
			return isShift ? 'Ç' : 'ç';
		case KEY_QUOTATION:
			return isShift ? '"' : '\'';
		// ///////////////////////
		case KEY_NUM_DIVISION:
			return '/';
		case KEY_NUM_MULTIPLY:
			return '*';
		case KEY_NUM_MINUS:
			return '-';
		case KEY_NUM_PLUS:
			return '+';
		case KEY_DOT:
			return '.';
		case KEY_NUM_PERIOD:
			return ',';
		case KEY_HYPHEN:
			return isShift ? '_' : '-';
		case KEY_EQUAL:
			return isShift ? '+' : isAlt && isCtrl ? '§' : '=';
		case KEY_RIGHT_BAR:
			return isShift ? '?' : isAlt && isCtrl ? '°' : '/';
		case KEY_LEFT_BAR:
			return isShift ? '|' : '\\';
		case KEY_SHARP_ACCENT:
			return isShift ? '`' : '´';
		case KEY_MATCH_RIGHT:
			return isShift ? '}' : isAlt && isCtrl ? 'º' : ']';
		case KEY_MATCH_LEFT:
			return isShift ? '{' : isAlt && isCtrl ? 'ª' : '[';
		case KEY_TIL_ACCENT:
			return isShift ? '^' : '~';
		case KEY_COMMA:
			return isShift ? '<' : ',';
		case KEY_FINAL_DOT:
			return isShift ? '>' : '.';
		case KEY_SEMICOLON:
			return isShift ? ':' : ';';
		case KEY_SPACE:
			return ' ';
		default:
			throw new IllegalArgumentException("Unknow key code " + keyCode);
		}
	}

	public static boolean isDigit(final int keyCode, final boolean isAlt, final boolean isCtrl, final boolean isShift) {

		switch (keyCode) {
		case KEY_ZERO:
		case KEY_ONE:
		case KEY_TWO:
		case KEY_THREE:
		case KEY_FOUR:
		case KEY_FIVE:
		case KEY_SIX:
		case KEY_SEVEN:
		case KEY_EIGHT:
		case KEY_NINE:
			if (isShift || isAlt || isCtrl) {
				return false;
			}
		case KEY_NUM_ZERO:
		case KEY_NUM_ONE:
		case KEY_NUM_TWO:
		case KEY_NUM_THREE:
		case KEY_NUM_FOUR:
		case KEY_NUM_FIVE:
		case KEY_NUM_SIX:
		case KEY_NUM_SEVEN:
		case KEY_NUM_EIGHT:
		case KEY_NUM_NINE:
			return true;
		default:
			return false;
		}
	}

